#include <iostream>

using namespace std;
int main(){
   int x=7, y=3; // Declare and initialize variables x and y
    cout << "sum of x + y = " << x + y << endl; // Addition
    cout << "sub of  x - y = " << x - y << endl; // Subtraction
    cout << "mul of  x * y = " << x * y << endl; // Multiplication
    cout << "div of x / y = " << x / y << endl; // Division
    cout << "mod of x % y = " << x % y << endl; // Modulus
}
